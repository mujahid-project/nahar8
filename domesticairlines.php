                            <h2 class="heading">With low-rise cottages scattered through acres of tropical gardens this is a popular resort with plentiful facilities.</h2>

                        </div>
                        <div class="k_fact introduction_text" data-fact_type="WebText">


                            <p>This Paradisus property is a great choice if you are seeking a resort with plenty of activities and facilities, alongside a beautiful beach and gardens where you can find a quiet spot of your own to relax. With unlimited green fees at the nearby Cocotal Golf Course, a tranquil YHI spa set in the tropical gardens, and a comprehensive watersports program, you have all the essentials for a perfect Caribbean holiday. </p><p>As would be expected from a Paradisus resort, there are a wide variety of dining venues offering cuisine from around the world and both casual and formal atmospheres. The shining star is the specialist restaurant Passion by Martin Berasategui, a renowned chef who has been awarded seven Michelin Stars throughout his career. The main buffet restaurant Naos is open throughout the day for all, while the beachfront restaurant La Palapa is reserved for Royal Service guests. </p><p>The resort is set in three distinct areas – the main resort, Royal Service and the secluded Reserve. Junior Deluxe Paradisus Suites and Luxury Junior Suites are set throughout the main resort in low-rise buildings, overlooking the tropical gardens. Well worth the upgrade is the selection of Royal Service Junior Suites – an adults-only enclave – or The Reserve, a top choice for families. This haven is set at the back of the resort in the midst of tropical gardens. If you opt for a One Bedroom Family Concierge Suite or The Reserve One Bedroom Suite you can use all the resort’s main facilities and you’ll also have use of an exclusive pool and private beach area, as well the Gabi Club Restaurant.</p>
